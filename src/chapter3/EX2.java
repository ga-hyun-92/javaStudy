package chapter3;

public class EX2 {

	public static void main(String[] args) {
		// if-else문 예제
		int num=50;
		if (num>=50) {
			System.out.println("50이상의 값이 들어있습니다.");
		} else {
			System.out.println("50미만의 값이 들어있습니다.");
		}
		
		double height=150.1;
		if (height>150) {
			System.out.println("탑승가능");
		} else {
			System.out.println("탑승불가");
		}
		

	}

}
